class Dog():
    """Создание класса Dog"""

    def dog_say(self):
        """Создание метода dog_say"""
        print('Gav-Gav')


class Cat():
    """Создание класса Cat"""

    def cat_say(self):
        """Создание метода cat_say"""
        print('Myu-Myu')


Sharik = Dog()
Tom = Cat()

Sharik.dog_say()
Tom.cat_say()
